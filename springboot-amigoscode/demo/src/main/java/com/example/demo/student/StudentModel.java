package com.example.demo.student;

import java.time.LocalDate;

//En ese modelo se creará la estructura de las bases de datos
public class StudentModel {
    //Se declaran las variables
    private Long id;
    private String name;
    private String email;
    private LocalDate dob; //LocalDate sirve para guardar fechas, en este caso vamos a guardar su fecha de nacimiento
    private Integer age;

    //Constructor vacío
    public StudentModel() {
    }

    //Construtor con todos los atributos
    public StudentModel(Long id, String name, String email, LocalDate dob, Integer age) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.dob = dob;
        this.age = age;
    }

    //Constructo sin ID, este va sin ID porque la DB generará el ID automáticamente

    public StudentModel(String name, String email, LocalDate dob, Integer age) {
        this.name = name;
        this.email = email;
        this.dob = dob;
        this.age = age;
    }

    // Getters y Setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public LocalDate getDob() {
        return dob;
    }

    public void setDob(LocalDate dob) {
        this.dob = dob;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    // Método toString

    @Override
    public String toString() {
        return "StudentModel{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                ", dob=" + dob +
                ", age=" + age +
                '}';
    }
}
